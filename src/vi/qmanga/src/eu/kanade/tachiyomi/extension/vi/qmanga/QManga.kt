    override fun fetchSearchManga(
        page: Int,
        query: String,
        filters: FilterList,
    ): Observable<MangasPage> {
        return when {
            query.startsWith(PREFIX_ID_SEARCH) -> {
                val id = query.removePrefix(PREFIX_ID_SEARCH).trim()
                val url = "/$id.html"
                fetchMangaDetails(
                    SManga.create().apply {
                        this.url = url
                    },
                )
                    .map {
                        it.url = url
                        MangasPage(listOf(it), false)
                    }
            }
            else -> super.fetchSearchManga(page, query, filters)
        }
    }

    override fun searchMangaRequest(page: Int, query: String, filters: FilterList): Request {
        val url = baseUrl.toHttpUrl().newBuilder().apply {
            if (query.isNotBlank()) {
                addPathSegment("tim-kiem")
                addQueryParameter("q", query)
                addQueryParameter("page", page.toString())
            } else {
                // the `comedy` part doesn't really matter, it just wants a thing here to load
                // the filtering page
                addPathSegments("danh-muc/comedy")
                (if (filters.isEmpty()) getFilterList() else filters)
                    .filterIsInstance<UriPartFilter>()
                    .forEach {
                        addQueryParameter(it.query, it.toUriPart())
                    }
            }
        }.build().toString()
        return GET(url, headers)
    }


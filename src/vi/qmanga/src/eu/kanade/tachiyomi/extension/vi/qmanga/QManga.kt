    override fun mangaDetailsParse(document: Document): SManga = SManga.create().apply {
        title = document.select(".title-commic-detail").text()
        author = document.select(".writer li").joinToString { it.text() }
        description = document.select(".desc-commic-detail").text()
        genre = document.select(".categories-list-detail-commic li a").joinToString { it.text() }
        thumbnail_url = document.select(".img-commic-detail img").attr("abs:data-src")

        val statusString = document.select(".status_commic p").text()
        status = when (statusString) {
            "Đang tiến hành" -> SManga.ONGOING
            "Đã hoàn thành" -> SManga.COMPLETED
            else -> SManga.UNKNOWN
        }
    }

    override fun chapterListSelector(): String = ".ul-list-chaper-detail-commic li"

    override fun chapterFromElement(element: Element): SChapter = SChapter.create().apply {
        setUrlWithoutDomain(element.select("a").attr("href"))
        name = element.select("a h3").text()

        val dateUploadString = element.select("span").first()?.text()
        if (!dateUploadString.isNullOrEmpty()) {
            date_upload = runCatching {
                dateFormat.parse(dateUploadString)?.time
            }.getOrNull() ?: 0L
        }
    }

    override fun pageListParse(document: Document): List<Page> {
        // as an anti-scraper measure, you need to type a given keyword and complete
        // a reCAPTCHA to unlock some chapters. i'm not doing that.
        val isLocked = document.select(".lock-screen").first() != null
        if (isLocked) {
            throw Exception("Vui lòng mở khoá chương truyện này trong WebView")
        }

        return document.select("img.image-commic").mapIndexed { idx, it ->
            Page(idx, imageUrl = it.attr("abs:src"))
        }
    }

